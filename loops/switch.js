let fruit='banana';
let fruitcolour;

switch(fruit){

 case "apple" :
    fruitcolour = "red";    break;

    case "banana" :
    fruitcolour = "yellow";    break;

    case "grapes" :
    fruitcolour = "green";    break;

    default :
    fruitcolour = "not found fruit colour";   
}
console.log(fruitcolour);